#!/bin/bash

# –ë—ã—Å—Ç—Ä–∞—è —É—Å—Ç–∞–Ω–æ–≤–∫–∞ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞ Let's Encrypt –¥–ª—è –¥–æ–º–µ–Ω–∞
# –î–ª—è –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –Ω–∞ —É–∂–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–Ω–æ–º —Å–µ—Ä–≤–µ—Ä–µ

set -e

GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m'

echo -e "${GREEN}üîí –£—Å—Ç–∞–Ω–æ–≤–∫–∞ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞ Let's Encrypt${NC}"
echo "=============================================="
echo ""

# –ü—Ä–æ–≤–µ—Ä–∫–∞, —á—Ç–æ —Å–∫—Ä–∏–ø—Ç –∑–∞–ø—É—â–µ–Ω —Å sudo/root
if [ "$EUID" -ne 0 ]; then
    echo -e "${RED}–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –∑–∞–ø—É—Å—Ç–∏ —Å–∫—Ä–∏–ø—Ç —Å sudo:${NC}"
    echo "sudo bash install-ssl-only.sh"
    exit 1
fi

# –ó–∞–ø—Ä–æ—Å –¥–æ–º–µ–Ω–∞
echo -e "${YELLOW}–í–≤–µ–¥–∏ –¥–æ–º–µ–Ω (–Ω–∞–ø—Ä–∏–º–µ—Ä: liks.ru):${NC}"
read DOMAIN

echo -e "${YELLOW}–í–≤–µ–¥–∏ email –¥–ª—è —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–π –æ SSL:${NC}"
