# –£—Å—Ç–∞–Ω–æ–≤–∫–∞ Certbot
echo -e "${GREEN}[2/3] –£—Å—Ç–∞–Ω–æ–≤–∫–∞ Certbot...${NC}"
apt install -y certbot python3-certbot-nginx

# –ü–æ–ª—É—á–µ–Ω–∏–µ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞
echo -e "${GREEN}[3/3] –ü–æ–ª—É—á–µ–Ω–∏–µ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞...${NC}"
echo ""
echo -e "${YELLOW}Certbot —Å–µ–π—á–∞—Å –ø–æ–ª—É—á–∏—Ç —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç –∏ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –Ω–∞—Å—Ç—Ä–æ–∏—Ç Nginx${NC}"
echo ""

# –ü—Ä–æ–≤–µ—Ä–∫–∞, —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –ª–∏ Nginx
if systemctl is-active --quiet nginx; then
    echo "Nginx —Ä–∞–±–æ—Ç–∞–µ—Ç ‚úì"
else
    echo -e "${RED}Nginx –Ω–µ –∑–∞–ø—É—â–µ–Ω! –ó–∞–ø—É—Å–∫–∞–µ–º...${NC}"
    systemctl start nginx
fi

# –ü–æ–ª—É—á–µ–Ω–∏–µ —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞ —Å –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–π –Ω–∞—Å—Ç—Ä–æ–π–∫–æ–π Nginx
certbot --nginx -d $DOMAIN -d www.$DOMAIN \
    --non-interactive \
    --agree-tos \
    --email $EMAIL \
    --redirect

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —É—Å–ø–µ—à–Ω–æ—Å—Ç–∏
if [ $? -eq 0 ]; then
    echo ""
    echo -e "${GREEN}=============================================="
    echo "‚úÖ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç —É—Å–ø–µ—à–Ω–æ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω!"
    echo "==============================================\${NC}"
    echo ""
    echo "–¢–≤–æ–π —Å–∞–π—Ç —Ç–µ–ø–µ—Ä—å –¥–æ—Å—Ç—É–ø–µ–Ω –ø–æ HTTPS:"
    echo -e "${GREEN}https://$DOMAIN${NC}"
    echo ""
    echo "–ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–æ!"
    echo ""
    echo "–ü—Ä–æ–≤–µ—Ä–∏—Ç—å —Å—Ç–∞—Ç—É—Å:"
    echo "  certbot certificates"
    echo ""
    echo "–¢–µ—Å—Ç–æ–≤–æ–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ:"
    echo "  certbot renew --dry-run"
    echo ""
    echo -e "${GREEN}–¢–µ–ø–µ—Ä—å –º–æ–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –≤ Telegram Web App!${NC}"
else
    echo ""
    echo -e "${RED}=============================================="
    echo "‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç–∞"
    echo "==============================================\${NC}"
    echo ""
    echo "–í–æ–∑–º–æ–∂–Ω—ã–µ –ø—Ä–∏—á–∏–Ω—ã:"
    echo "1. –î–æ–º–µ–Ω –Ω–µ —É–∫–∞–∑—ã–≤–∞–µ—Ç –Ω–∞ —ç—Ç–æ—Ç —Å–µ—Ä–≤–µ—Ä (–ø—Ä–æ–≤–µ—Ä—å DNS)"
    echo "2. –ü–æ—Ä—Ç 80 –∑–∞–∫—Ä—ã—Ç —Ñ–∞–π—Ä–≤–æ–ª–æ–º"
    echo "3. Nginx –Ω–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω –∏–ª–∏ –Ω–µ –∑–∞–ø—É—â–µ–Ω"
    echo ""
    echo "–ü—Ä–æ–≤–µ—Ä—å DNS:"
    echo "  dig $DOMAIN +short"
    echo ""
    echo "–ü—Ä–æ–≤–µ—Ä—å Nginx:"
    echo "  systemctl status nginx"
    echo "  nginx -t"
fi
